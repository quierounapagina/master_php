/*
Ejercicio 3:
Escribir un programa que imprima por pantalla los cuadrados (Numeros multiplicados por si mismo)
de los primeros 40 primeros numeros naturales, Usar Bucle While.
*/

$i=1;
while ($i<=40){
    $cuadrado= $i*$i;
    echo "El cuadrado de $i es $cuadrado <br/>";
    $i++;
}


